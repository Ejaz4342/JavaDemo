package Arrays;

public class ArrayDemo3 {
    public static void main(String[] args) {
        int[] arr1=new int[5];
        arr1[0]=10;
        arr1[1]=20;
        arr1[2]=30;
        arr1[3]=40;
        arr1[4]=50;

        // printing Array elements using for loop

        for (int row=0;row<=4;row++){
            System.out.println(arr1[row]);
        }
        // *** we can also wirte like row<5 ***.



    }
}
